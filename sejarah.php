        <p>Untuk mengenang peristiwa yang hampir mengancam eksistensi ideologi bangsa Indonesia, pemerintahan Orde Baru menetapkan 1 Oktober sebagai Hari Kesaktian Pancasila melalui Surat Keputusan Menteri/Panglima Angkatan Darat tanggal 17 September 1966 (Kep 977/9/1966).</p>
